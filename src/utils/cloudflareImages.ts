/**
 * Gets the Cloudflare image URL for a given local path
 * @param path Local image path 
 * @returns The Cloudflare URL if available, otherwise the original path
 */
export function getCloudflareUrl(path: string): string {
  // Extract just the filename from the path (strip any directory parts)
  const filename = path.split('/').pop() || '';
  
  // Return the Cloudflare URL if it exists, otherwise return the original path
  return imageMappings[filename] || path;
}
